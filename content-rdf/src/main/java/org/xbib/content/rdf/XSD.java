/**
 * Defines URIs for the XSD primitive datatypes.
 */
public interface XSD {

    String NS = "http://www.w3.org/2001/XMLSchema#";

    String ANY_URI = NS + "anyURI";
    String BASE64_BINARY = NS + "base64Binary";
    String DATE = NS + "date";
    String DATE_TIME = NS + "dateTime";
    String DECIMAL = NS + "decimal";
    String DURATION = NS + "duration";
    String ENTITIES = NS + "ENTITIES";
    String ENTITY = NS + "ENTITY";
    String G_DAY = NS + "gDay";
    String G_MONTH = NS + "gMonth";
    String G_MONTH_DAY = NS + "gMonthDay";
    String G_YEAR = NS + "gYear";
    String G_YEAR_MONTH = NS + "gYearMonth";
    String HEX_BINARY = NS + "hexBinary";
    String ID = NS + "ID";
    String IDREF = NS + "IDREF";
    String IDREFS = NS + "IDREFS";
    String INTEGER = NS + "integer";
    String LANGUAGE = NS + "language";
    String NAME = NS + "Name";
    String NC_NAME = NS + "NCName";
    String NEGATIVE_INTEGER = NS + "negativeInteger";
    String NMTOKEN = NS + "NMTOKEN";
    String NMTOKENS = NS + "NMTOKENS";
    String NON_NEGATIVE_INTEGER = NS + "nonNegativeInteger";
    String NON_POSITIVE_INTEGER = NS + "nonPositiveInteger";
    String NORMALIZED_STRING = NS + "normalizedString";
    String NOTATION = NS + "NOTATION";
    String POSITIVE_INTEGER = NS + "positiveInteger";
    String QNAME = NS + "QName";
    String TIME = NS + "time";
    String TOKEN = NS + "token";
    String UNSIGNED_BYTE = NS + "unsignedByte";
    String UNSIGNED_INT = NS + "unsignedInt";
    String UNSIGNED_LONG = NS + "unsignedLong";
    String UNSIGNED_SHORT = NS + "unsignedShort";
    String BOOLEAN = NS + "boolean";
    String BYTE = NS + "byte";
    String DOUBLE = NS + "double";
    String FLOAT = NS + "float";
    String INT = NS + "int";
    String LONG = NS + "long";
    String SHORT = NS + "short";
    String STRING = NS + "string";

}
